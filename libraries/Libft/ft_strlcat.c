#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t len_buff_dest)
{
	size_t	len_dest;
	size_t	len_src;
	size_t	i;

	len_dest = ft_strlen(dest);
	len_src = ft_strlen(src);
	if (len_buff_dest == 0)
		return (len_src);
	if (len_buff_dest <= len_dest)
		return (len_src + len_buff_dest);
	i = 0;
	while (i < (len_buff_dest - len_dest - 1) && src[i])
	{
		dest[len_dest + i] = src[i];
		i++;
	}
	dest[len_dest + i] = '\0';
	return (len_dest + len_src);
}
